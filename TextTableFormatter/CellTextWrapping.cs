namespace TextTableFormatter
{
    /// <summary>
    /// Represents a cell content wrapping style.
    /// </summary>
    public enum CellTextWrapping
    {
        /// <summary>
        /// Do not wrap cell content
        /// </summary>
        NoWrap,

        /// <summary>
        /// Wrap cell content
        /// </summary>
        Wrap
    }
